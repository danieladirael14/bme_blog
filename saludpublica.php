4.	Propuesta
En virtud de los resultados obtenidos, es necesario plantear una propuesta acorde a las necesidades que se han evidenciado, sin embargo, se deben proponer diseños integrales que abarquen todos los campos de acción, pero que también cumplan con los presupuestos de viabilidad factibilidad. En atención a ese requerimiento, la evidencia del proyecto justifica la siguiente propuesta:
•	Aumentar contratación de prestadores externos de salud, como red complementaria para el servicio de salud, los mismos que deben ser normados para que brinden servicios de salud de calidad a todos los ciudadanos.
Aumentar los procesos de convenios o contrataciones con prestadores externos es la mejor manera para auxiliar al trabajo de los hospitales pertenecientes al sistema de salud pública, de esa forma se pueden potencializar servicios que son patrocinados por el hospital público, pero auspiciados por los prestadores externos.


