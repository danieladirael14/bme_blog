2.	Marco teórico y desarrollo
2.1.	MARCO LEGAL
Según la Constitución en su Sección Séptima referente a la Salud en su Art. 32.- “La salud es un derecho que garantiza el Estado, cuya realización se vincula al ejercicio de otros derechos, entre ellos el derecho al agua, la alimentación, la educación, la cultura física, el trabajo, la seguridad social, los ambientes sanos y otros que sustentan el buen vivir.
El Estado garantizará este derecho mediante políticas económicas, sociales, culturales, educativas y ambientales; y el acceso permanente, oportuno y sin exclusión a programas, acciones y servicios de promoción y atención integral de salud, salud sexual y salud reproductiva. La prestación de los servicios de salud se regirá por los principios de equidad, universalidad, solidaridad, interculturalidad, calidad, eficiencia, eficacia, precaución y bioética, con enfoque de género y generacional.”
La Constitución en su Art. 360.- “El sistema garantizará, a través de las instituciones que lo conforman, la promoción de la salud, prevención y atención integral, familiar y comunitaria, con base en la atención primaria de salud; articulará los diferentes niveles de atención; y promoverá la complementariedad con las medicinas ancestrales y alternativas.
La red pública integral de salud será parte del sistema nacional de salud y estará conformada por el conjunto articulado de establecimientos estatales, de la seguridad social y con otros proveedores que pertenecen al Estado, con vínculos jurídicos, operativos y de complementariedad.”
El Reglamento para atención de Salud Integral y en red de los asegurados, menciona en su Art. 1.- “Objeto: El presente reglamento regula los procedimientos a seguir para brindar la cobertura efectiva en la atención de salud que requieran los asegurados del Instituto Ecuatoriano de Seguridad Social y, en general, los beneficiarios con derecho para acceder a las prestaciones de salud brindadas por las unidades médicas propias y externas de la Red Plural definidas en la cartera de servicios, conforme las Guías de Práctica Clínica y Terapéuticas de las Patologías y Procedimientos priorizados en todos los niveles de complejidad de los servicios de salud.”
En casi todos los países de América Latina, por lo menos en teoría, el Gobierno contribuye al sostenimiento del seguro social, de acuerdo con informaciones de la OEA, la mayoría de países latinoamericano confronta el problema de incapacidad financiera para extender al campesinado sus sistemas de seguridad social, especialmente en el campo de las prestaciones de salud.
El estado ecuatoriano es garante del derecho a la salud a través de la formulación de políticas, planes y programas orientados a brindar acceso a servicios de calidad, eficiencia, eficacia, precaución y bioética, con enfoque de género y generacional. Es así, que el mandato constitucional determina la necesidad de iniciar el proceso de reforma y definir el ámbito en el cual se conducirá dicho proceso para lograr que el Estado cumpla con las nuevas obligaciones establecidas en la Constitución .

2.2.	CALIDAD
2.2.1.	Calidad en el servicio de salud pública
El Estado debe garantizar la calidad, la eficiencia, la eficacia y el impacto de los servicios de salud pública a través de una legislación estricta para lograr la excelencia en estos servicios. La calidad es una medida de lo bien que algo satisface una necesidad, resuelve un problema o añade valor a alguien .
La calidad de la asistencia sanitaria es un tema relevante hoy en día debido a la libertad de los usuarios para opinar sobre los servicios que reciben, los proveedores y las instituciones responsables de estos servicios. Esto viene dictado por los retos organizativos y las crecientes presiones sociales, así como por la percepción que la sociedad tiene de sus derechos. (Guerreros, 2018)
Una excelente calidad del servicio de salud pública, merece la atención que abarca la promoción, la prevención, el tratamiento, la rehabilitación y la paliación e implica que la calidad de la atención pueda medirse y mejorarse continuamente mediante la prestación de una atención basada en las necesidades de los pacientes. La sensibilidad de la populación para la salud pública ha ido aumentando y se ve reflejada en diversificadas iniciativas que están siendo llevadas a cabo para incrementar la calidad del cuidado y el apoyo en las demandas del público. (OMS, 2020)
La satisfacción del paciente dentro de un centro de salud es un resultado concreto de la interacción entre factores positivos que afectan a los profesionales de la salud y a los pacientes, por lo que la interacción entre ambos produce beneficios de carácter social que aumentan la satisfacción y complacencia del grupo social, encargado de percibir y evaluar la calidad. de los sistemas de salud. (De Los Ríos & Ávila, 2004)
Para la Organización Mundial de la Salud (OMS) (2020), la calidad exigida a los centros de salud, ya sean públicos o privados, debe garantizar que "los pacientes reciban los procesos de evaluación, diagnóstico y tratamiento más adecuados de la mejor manera posible para lograr una atención de alta calidad".
La calidad llega a las personas que reciben el tratamiento, que proviene de la atención prestada por los profesionales sanitarios y del servicio de atención al cliente de las instituciones encargadas de velar por la salud de los ciudadanos. La calidad en la salud pública es un instrumento que permite, además de mejorar la calidad de vida de las personas, otorga la capacidad para generar cambios sociales y económicos importantes. (Fariño y otros, 2018)
La ACESS Agencia de Aseguramiento de la Calidad de los Servicios de Salud y Medicina Prepagada (2015), tiene como objetivo  vigilar y controlar la calidad de los servicios prestados por los prestadores de salud y las empresas que financian servicios integrales de salud prepagada, así como por las aseguradoras que ofrecen seguros de salud, garantizando la seguridad de los pacientes y usuarios a través de la regulación y la garantía de calidad y dentro de un enfoque de género, intercultural, intergeneracional y de derechos bioéticos.

