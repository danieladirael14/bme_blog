2.2.2.	Satisfacción del servicio
Hayes (2002) indica que no existe una definición específica de satisfacción del usuario que da respuestas intelectuales, materiales y emocionales ante el cumplimiento de una necesidad o deseo de información, este estado se convierte en un juicio de evaluación porque se comparan los objetivos y expectativas contra los resultados obtenidos, la percepción de la calidad está subordinada a factores como las expectativas, valores morales, culturales, necesidades personales, retribuciones esperadas, información recogida de otros usuarios y de la propia organización sanitaria. Estos elementos condicionan a que la satisfacción sea diferente para distintas personas y para la misma persona en diferentes circunstancias. 
Según Guerreros (2018), la satisfacción del usuario es el sentimiento personal de placer o decepción que resulta de la comparación entre el rendimiento percibido de un producto y sus expectativas. 
Para lograr la satisfacción del paciente, las personas que lo atienden deben estar altamente calificadas para mostrar cortesía, atención, respeto, paciencia, rapidez, eficiencia y privacidad, así como las instalaciones que brinda el centro de salud, es decir, que los procedimientos administrativos sean correctos para lograr la satisfacción del usuario, además de los profesionales de la salud que brinden una buena información al paciente, certeza y tranquilidad sobre la enfermedad que lo afecta actitud. (Borré, 2013)
Si el rendimiento está por debajo de las expectativas, los clientes estarán descontentos. Si el rendimiento está por debajo de las expectativas, los clientes estarán insatisfechos. Si el rendimiento está dentro de las expectativas, el cliente estará satisfecho. Si el rendimiento supera las expectativas, el cliente estará muy satisfecho. (Da Silva, 2021)

2.2.3.	Servicios públicos
Villegas (1949) define servicio público como toda actividad directa o indirecta de la administración pública que tiene por objeto satisfacer necesidades colectivas mediante procedimientos de derecho público.
El servicio público es una categoría jurídica y económica, y desde el punto de vista constitucional, es una garantía constitucional que cumple la función de acceso a derechos como el agua, la electricidad, el saneamiento, la educación, la salud, el transporte, etc. (Albuja, 2021)
Diez (1981) limita el concepto de servicio público a aquel que es "prestado directa o indirectamente por la administración para satisfacer las necesidades del interés general". Acentuándose a la idea clásica de que el servicio público es "algo" ofrecido a los ciudadanos independientemente.
Como acertadamente conceptualiza el abogado ecuatoriano Efraín Pérez (2009), el contexto jurídico más importante de los servicios públicos es que el Estado tiene el derecho exclusivo de prestarlos directamente o a través de concesionarios, con sujeción a determinadas normas. Al impedir el libre desarrollo de estas actividades por parte de los particulares, la calidad de servicio público debe ser declarada por normas constitucionales o legales. 
Los servicios públicos tienen una relación directa con el usuario, el diseño y prestación de estos servicios está bajo la Administración Pública quien es la responsable de garantizar servicios de calidad, cumpliendo estándares técnicos, logrando el derecho ciudadano de un servicio público que satisfaga sus necesidades. (Servicio de acreditación ecuatoriano, 2018)

2.3.	Prestación de servicios de salud pública
La salud pública hoy en día es más compleja que hace unos años. Requiere no solo el desarrollo de prácticas técnicas basadas en la ciencia, sino también las habilidades y destrezas para crear alianzas y tomar medidas para mejorar la salud. La gestión basada en la salud se conoce como gestión de la salud pública. En el sentido más filosófico, el concepto de salud pública se refiere al proceso de movilización de recursos para asegurar condiciones adecuadas para un estado de salud sostenible de una población. (Villalbí, 2012)
Los servicios de salud pública son los recursos organizados para mejorar la salud pública dentro de un departamento administrativo. Suelen estar agrupados en una estructura específica del sector sanitario, a menudo denominada autoridad sanitaria pública u organismo similar. En cualquier caso, la existencia de los servicios y su organización en una sociedad depende en gran medida de la historia, la cultura organizativa y otras variables: no es raro que los servicios de salud pública estén integrados en otras unidades administrativas. (Villalbí, 2012)
La Constitución de la República del Ecuador (2008) manifiesta en su Art. 362.- “La atención de salud como servicio público se prestará a través de las entidades estatales, privadas, autónomas, comunitarias y aquellas que ejerzan las medicinas ancestrales alternativas y complementarias. Los servicios de salud serán seguros, de calidad y calidez, y garantizarán el consentimiento informado, el acceso a la información y la confidencialidad de la información de los pacientes.
Los servicios públicos estatales de salud serán universales y gratuitos en todos los niveles de atención y comprenderán los procedimientos de diagnóstico, tratamiento, medicamentos y rehabilitación necesarios.”
La prestación de los servicios de salud pública, demandan del suficiente recurso humano competente, poli funcional y acorde a las necesidades de atención de la colectividad. Debe contar con un sistema de información, monitoreo, evaluación y supervisión en corresponsabilidad con los actores del sector salud, además, para brindar un óptimo servicio se necesita equipamiento e insumos adecuados de acuerdo a las necesidades del sector salud. (De Habich & Del Carmen, 2013)








