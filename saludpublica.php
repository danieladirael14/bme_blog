2.4.3.	Prestación de servicios
El Estado cuanta con varios mecanismos de organización para el cumplimiento de sus objetivos fundamentales, como empresas que realicen la prestación de servicios. (Asamblea Nacional, 2015)
La prestación de estos servicios es un contrato de la administración pública en el sentido de que se trata de un acuerdo con disposiciones contractuales, redactado previamente según las disposiciones de estos instrumentos. Este método de prestación de servicios ha ganado relevancia en las últimas décadas porque es rentable y consigue el éxito deseado, para lo que se necesitan métodos modernos de producción y logística. (Alonso, 2016)

2.4.3.1.	Prestadores externos de salud
El Estado ofrece servicios sanitarios públicos gratuitos. En los últimos años, los centros hospitalarios primarios no han podido atender a todos los usuarios y la demanda de los pacientes ha aumentado considerablemente. Ante esta carencia, el IESS ha firmado convenios con centros médicos privados que, tras cumplir una serie de requisitos, obtener la calificación del IESS y la licencia del organismo regulador, pueden prestar servicios sanitarios integrales a todos los afiliados de forma eficiente y responsable. (Gómez & Rivera, 2019)
La Red Pública Integrada de Salud (RIPS) está constituida por el MSP, el ISSE, el ISSFA, las unidades de salud del ISSPOL bajo convenios marco interinstitucionales y los establecimientos de salud privados que complementan la red pública y constituyen una red integrada de servicios de salud. Su objetivo es promover e implementar mecanismos de acceso universal a servicios sanitarios de calidad, equitativos, gratuitos y eficientes para la población, optimizando la organización y gestión de la red asistencial en todos los niveles del sistema nacional de salud. (MSP, 2015)
Existe el Reglamento para atención de salud integral y en red de los asegurados (2011), y, según la resolución del IESS, el organismo pretende fortalecer sus unidades médicas en todo el país hasta que estén 100% operativas, pero este es un proceso largo, por lo que es necesario contratar servicios con proveedores de salud externos.



 
