El presente proyecto tuvo objetivos que guiaron el marco de la investigación, de ello se puede concluir:
•	Se realizó un análisis de bibliografías referidas a la calidad del Servicio de Salud Pública, para poder una base teórica y documental para establecer los puntos clave que ayudarían a la recolección de información mediante las técnicas de investigación. La recolección de bibliografías justificó y nutrió el marco teórico, que permitió dar luces sobre la investigación que se lleva, dando cumplimiento al objetivo específico número 1
