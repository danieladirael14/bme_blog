2.4.	SERVICIOS EXTERNOS
2.4.1.	Concepto
Externalización es contratar la prestación de una misma actividad por un tercero ajeno a la empresa. Su origen proviene en el siglo XIX, cuando empresas de Estados Unidos de América (EEUU) externalizaron la fabricación de velas para veleros y cubre vagones a trabajadores de Escocia, quienes a su vez importaban la materia prima desde India. (Toyama, 2008)
Mediante el uso estratégico de la subcontratación, la organización se especializa en actividades de valor añadido y aprovecha la experiencia y el potencial de innovación del sector privado, lo que se traduce en bienes y servicios de alta calidad y en un aumento de la eficiencia. (Matas & García, 2006)
Se realiza la búsqueda de servicios externos cuando la empresa no posee dichos servicios y el hecho de implementarlos resulta un gasto gigante, de esta manera los clientes, en el caos de la salud pública, pacientes, satisfacen sus necesidades sin dejar de contar con el servicio de salud pública. (Toyama, 2008)

