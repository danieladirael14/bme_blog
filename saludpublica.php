1.	Introducción
El objetivo principal de los Servicios Públicos es satisfacer las demandas y necesidades de la población. Estos servicios son prestados directamente por el Estado o bajo su control y regulación, se prestan sin ánimo de lucro y contribuyen al bienestar social de los ciudadanos, la Constitución de la República del Ecuador (2008) la cual, en su Art. – 52 menciona “Las personas tienen derecho a disponer de bienes y servicios de óptima calidad y a elegirlos con libertad, así como a una información precisa y no engañosa sobre su contenido y características.” También podemos encontrar el Art. 85.- en su inciso 1. “Las políticas públicas y la prestación de bienes y servicios públicos se orientarán a hacer efectivos el buen vivir y todos los derechos, y se formularán a partir del principio de solidaridad.” … El Art. Art. 361.- “El Estado ejercerá la rectoría del sistema a través de la autoridad sanitaria nacional, será responsable de formular la política nacional de salud, y normará, regulará y controlará todas las actividades relacionadas con la salud, así como el funcionamiento de las entidades del sector.”
El sistema de salud pública de Ecuador está a cargo del Ministerio de Salud Pública (MSP), el Ministerio de Inclusión Económica y Social (MIESS), el Instituto Ecuatoriano de Seguridad Social (IESS), el Instituto de Seguridad Social de las Fuerzas Armadas (ISSFA) y el Instituto de Seguridad Social de la Policía Nacional (ISSPOL), siendo el MSP el que presta servicios de salud a la población en general y el MIESS, a través de sus programas, el que también presta servicios de salud a la población no asegurada. Los servicios de salud son prestados por el IESS, ISSFA e ISSPOL a la población ocupada que ha sido desvinculada de estas entidades.
Según la Ley Orgánica de Salud menciona en su Art 3.- “La salud es el completo estado de bienestar físico, mental y social y no solamente la ausencia de afecciones o enfermedades. Es un derecho humano inalienable, indivisible, irrenunciable e intransigible, cuya protección y garantía es responsabilidad primordial del Estado; y, el resultado de un proceso colectivo de interacción donde Estado, sociedad, familia e individuos convergen para la construcción de ambientes, entornos y estilos de vida saludables.”; y, en su Art. 4.- “La autoridad sanitaria nacional es el Ministerio de Salud Pública, entidad a la que corresponde el ejercicio de las funciones de rectoría en salud; así como la responsabilidad de la aplicación, control y vigilancia del cumplimiento de esta Ley; y, las normas que dicte para su plena vigencia serán obligatorias.”
En Ecuador existen variedad de establecimientos para atender la salud de los ciudadanos, en el caso de Guayaquil, según la sectorización del Ministerio de Salud, esta ciudad pertenece a la Zona 8, y podemos encontrar algunos centros de salud, adicional a estos centros de salud, encontramos los hospitales del IESS, sumamos los establecimientos del ISSFA (Instituto de Seguridad Social de las Fuerzas Armadas) y los del ISSPOL (Instituto de Seguridad Social de la Policía Nacional).
En Ecuador más del 90 % de la población es atendida por el servicio de salud pública, el porcentaje restante de la población que pertenece a la clase social media y alta que paga un seguro privado o medicina prepagada, para ser atendidos en consultorios u hospitales privados, lo mismo ocurre en Guayaquil, que cuenta con alrededor de 2´698.077 de habitantes en la ciudad y sus alrededores según los datos del INEC (Instituto Nacional de Estadísticas y Censos).
A pesar de que en Guayaquil hay más de 90 sitios de atención médica que tiene el MSP, centros de salud pública (primer nivel) hospitales del día y generales (segundo nivel) y hospitales de especialidad (tercer nivel), estos no cumplen con las expectativas, ya que, los trámites burocráticos son muy largos y no brindan a los pacientes una atención oportuna, ya que ellos, primero deben atenderse en el centro de salud más cercano, para luego ser derivados, dependiendo diagnóstico del doctor de turno, es decir, si el estado de salud del paciente lo amerita, este es derivado a un hospital de segundo o tercer nivel según sus necesidades, este trámite lleva tiempo lo cual es muy perjudicial para la salud de los pacientes, sumado a esto la escases de medicina, la falta de personal, estas instituciones no cumplen con las necesidades básicas y oportunas para la atención de salud, lo mismo ocurre en el IESS, ISSFA, ISSPOL. .

1.1.	Justificación del tema elegido
La prestación de Servicios de Salud Pública es esencial y está dirigida por el Ministerio de Salud Pública, que es responsable de proporcionar servicios sanitarios de calidad a todos los ciudadanos. La sociedad moderna requiere de un modelo de mejora que garantice una excelente funcionalidad y desempeño de los servicios públicos de salud en todo el país, por lo tanto, estas mejoras se verán reflejadas en los servicios que se prestan en Guayaquil y las mejoras que se desarrollen en todas las áreas de la salud deberán atender directamente los problemas que se presentan por la inadecuada prestación de los servicios de salud, de ahí la atención oportuna a todos los usuarios.
Una decisión para solventar la ineficiente atención a los ciudadanos, es la de contratar servicios externos - Prestadores de Salud -, que son aquellos profesionales o establecimientos, autorizados para prestar servicios de salud debidamente certificados/acreditados por la Autoridad Sanitaria Nacional que mantienen un contrato vigente con el Instituto Ecuatoriano de Seguridad Social.
En el 2019 el IESS expide la Resolución Administrativa No. IESS-DG-AL-2019-002-RFDQ (2019) sobre La delegación y normas generales para la suscripción de instrumentos legales con prestadores de servicios de salud de la red privada complementaria, en sus Disposiciones Generales – Segunda. – “La suscripción de instrumentos de prestaciones de salud con la red privada complementaria se efectuará de conformidad a las disposiciones que, para el efecto, expida el Ministerio de Salud Pública y la Dirección General de Salud, Individual y Familiar.”     
Además, el presente trabajo se justifica de forma práctica debido a que sirve como base para la aplicación física de una mejora en el servicio de salud pública del Ecuador, enfocado en la ciudad de Guayaquil.

1.2.	 Problema y finalidad del trabajo
En Ecuador, los servicios de salud pública, no abastecen las necesidades de la colectividad, ya que los usuarios tienen que soportar una larga espera para ser atendidos, y las instituciones que brindan estos servicios no cuentan con infraestructura adecuada, algunos equipos no están operativos o no los tienen, falta personal y la medicina es escasa. 
Finalmente, en respuesta a lo anterior, consideramos necesario hacer una propuesta de mejora del servicio público de salud en Guayaquil para fortalecer la gestión actual y evitar la gestión ineficiente en la realización de actividades que no generan valor, sino que aumentan la burocracia y retrasan el producto a conseguir, una jerarquía vertical que sigue latente en las instituciones del Ministerio de Salud, con dirigentes que manejan todos los niveles del sistema de salud, lo cual es bien conocido. Dejando claro que la finalidad de este trabajo es dejar como manifiesto que es necesario la mejora urgente de los servicios de salud pública, ya que están afectando la calidad de vida de los habitantes, se debe realizar una revisión constante de la normativa y establecer un plan de mejora. Una posible solución, sería implementar los servicios externos de salud, con una clara normativa para operar en beneficio de los usuarios.

