	Diagnóstico Situacional.
“Es la etapa más temprana del proyecto, dado que es la revisión y evaluación del estado que se encuentra la entidad tanto en interna como externa, se realiza varios análisis en referencia en debilidades, fortalezas, competidores, en el ambiente donde se realiza las rivalidades del mercado y donde se refleja si toman las mejores decisiones frente a las situaciones de emergencia en la compañía a diario” (J, 2015).
	Análisis Interno.
Este análisis se basa en el diagnóstico que se realiza de manera interna, dado que se percibe más la calidad de producto o servicio que ofrece la compañía, sus recursos, las debilidades, fortalezas, las amenazas que contribuyen en la corporación de la entidad como tal.
Según “la empresa debe identificar cuáles son los recursos capaces de proporcionar una ventaja competitiva sustentable y una corriente de renta mantenida a largo plazo, de ahí que suela destacarse, a la hora de caracterizar y definir el concepto de cultura organizativa, la importancia que esta tiene como elemento diferenciador” (Acosta, 2008)
	Análisis externo.
El análisis externo es aquel que ayuda a una empresa a identificar y evaluar todos los factores externos como acontecimientos, situaciones y tendencias que afectan a su desempeño, y que no puede controlar.
	Análisis PEST.
“El análisis PEST es una herramienta de medición de negocios que ayuda a comprender los comportamientos de mercado, tiene mucha relación con el análisis FODA, ya que ayuda a detectar predilecciones y acontecimientos claves del pasado, presente y futuro de la sociedad, compuesto por sus factores políticos, económicos, sociales y tecnológicos” (Martínez & Milla, 2012).
