La problemática de investigación que se lleva a cabo en la empresa “REGENERAMD S.A.”, con el que se realiza un plan estratégico para plasmar las propuestas y estrategias para maximizar su producción, su manejo de los insumos, materiales que se implementan a realizar el servicio, mejorando la calidad e incrementando sus ventas e ingresos por lo que este proyecto se va utilizando información primaria y secundaria para tener más precisión en los resultados para la organización.
