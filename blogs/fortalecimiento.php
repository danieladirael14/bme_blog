1.2.3. Etapas de la planeación. 
Las etapas de la planeación son las siguientes:
	Identificación del problema
	Desarrollo de alternativas
	Elección de la alternativa más conveniente
	Ejecución del plan.
Durante esta investigación se adapta en las 4 etapas Consenso estratégico, Diagnóstico situacional, definición de estrategias y objetivos y el último plan estratégico.
	Planeación Estratégica.
La planificación estratégica se podría definir como un plan de acción que emplea la administración como herramienta primordial para la viabilidad y crecimiento de la organización.
“La planeación estratégica no es nada más un conjunto de planes funcionales o una extrapolación de los presupuestos actuales; es un enfoque de sistemas para guiar una empresa durante un tiempo a través de su medio ambiente, para lograr las metas dictadas” (De la Cruz Diana, 2008).
“Dentro del concepto de la estrategia tenemos sé remota desde el antiguo de la historia, por lo que se determina los objetivos a seguir, por lo que sus seguimientos sean continuos para seguir lo acordado y que se cumpla lo establecido un grupo determinado de personas o instituciones” (Miriam, 2003)
	La importancia de la Planeación Estratégica. 
           Para la importancia de la planeación estratégica tenemos lo siguiente “La Planeación Estratégica (PE) es de fundamental importancia en organizaciones, dado que el éxito o supervivencia de ellas dependerá básicamente de su elaboración. Esta PE se materializa en un documento llamado normalmente Plan. Las siguientes líneas tienen como objetivo aclarar la importancia señalada” (Cásate, 2017 pp3).
	Finalidad de la planeación estratégica.
             “La planificación estratégica permite determinar la dirección de la empresa, y alinearla cada uno de los trabajadores que la integran. Cuando el líder de la compañía y/o empresa comparte su visión con sus trabajadores, la organización define el rumbo correcto para alcanzar los objetivos. Además, esto obliga a los ejecutivos a ver la planificación desde la macro perspectiva, señalando los objetivos centrales, de forma que las acciones diarias de los trabajadores los acerquen cada vez más a las metas propuestas” (L, 2003).
“Permite a la empresa conocer la situación actual, a través de un análisis de sus fortalezas, debilidades, oportunidades y amenazas. Para adecuarse a los cambios y las demandas que exige el entorno, para así lograr una mayor eficiencia y calidad en los productos o servicios” (El Telegrafo, 2016).
