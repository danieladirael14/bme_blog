En el transcurso del tiempo la sociedad a nivel internacional se va demostrando que las últimas décadas una verdadera constancia y evolución en las empresas, lo que promueve un aumento en ventas, una excelente calidad de servicio hacia los clientes, entre otras. Las empresas deben tener en consideración para lograr un excelente rendimiento en todos sus ámbitos, deben ser muy preciso en proponer planes de estrategias, marketing, entre otros que permitan mantener su incremento constante.
En este trabajo investigativo se basa en la planificación estratégica de un centro médico llamado “REGENERAMD S.A.” que se dedica a tratamientos médicos nuevos y ambulatorios, es una de las primeras en Latinoamérica en tener nueva tecnología en medicina regenerativa celular, lo cual es muy atractivo hacia el mercado, dando un posicionamiento en el mismo, mejorando el bienestar de la sociedad con esta innovación médica.
Esta planeación estratégica es para la empresa “REGENERAMD S.A.”, se va idealizando las estrategias con estudios previos para mantener en referencia las metodologías, lo que mantiene un correcto proceso de calidad de servicios hacia los clientes y un avance en su sociedad. Una de las variables para la organización sería los costos que representan los tratamientos dependiendo individualmente que necesite el consumidor y la compañía.
En la actualidad, la innovación para la creación e implementación de los planes estratégicos deben ser precisos, definidos a cumplir sus objetivos proyectados para las organizaciones, verificando la aceptación al público junto con las estrategias para ser lanzadas con eficiencia y eficacia en el mercado.
Para la propuesta se plantearon objetivos con el fin de evaluar la situación inicial de la empresa, posteriormente con un estudio de mercado entre los consumidores, basándose en la calidad y variedad del servicio que ofrece la empresa encontrando los puntos principales de la empresa, necesita modificar y con una proyección futura a mediano plazo que podemos ratificar la eficacia de la estrategia para la empresa.
