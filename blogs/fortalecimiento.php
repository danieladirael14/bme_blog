           La propuesta de un plan estratégico para le empresa REGENERAMED S.A. su objetivo es el fortalecimiento comercial junto con una restructuración de gestión, por lo que se s realiza el diagnóstico situacional de la empresa por lo que tenemos basados en la teoría que brinda muchas propuestas benéficas empresariales en la organización dando como resultado el cambio de entorno lo que coincide con (Tamayo, 2015) y en la planificación en diferentes medidas de tiempo, por lo que en el día de hoy se mantiene en importancia realizar un análisis interno y externo para mantener una situación controlada si se presenta una emergencia a futuro.
Tenemos en consideración que la empresa no ha mantenido un modelo empresarial ni alguna ideología sobre la dirección de la empresa junto con objetivos definidos, valores, lo que da como resultado falta de conocimientos para los departamentos al realizar el plan estratégico se procede a la elaboración, que conecta con (Castañón, 2015) en su artículo se detallan como fue la reestructuración de un plan estratégica que hicieron fortalecer las metas de la corporación. 
        Por su parte, Jiménez, V. (2017),  utilizó un balance Scorecard para medir y determinar si las propuestas puedes ser óptimas para la empresa por lo que se determinó indicadores, objetivos y los departamentos responsables de cada cumplimiento, lo que dio como resultados excelentes cambios del 80% lo que es un nivel óptimo, por lo que en este trabajo de investigación en la empresa REGENERAMED S.A. se realizó la implementación con el fin que sus fueron resultados positivos debido a que los sus indicadores fueron correctos que mantienen un rango del 70% de cumplimiento en cada área en especifica. 

	En culminación podemos tener en consideración que la empresa puede lograr mantener políticas que pueden llevar a una buena administración tanto interna como externa en la que se puede tener como metas alcanzar siempre y cuando se cumpla con las políticas organizacionales, por lo que llegan a los objetivos propuestos y aumentar en todos los ámbitos existentes. 
CONCLUSIONES Y RECOMENDACIONES
CONCLUSIONES

Se empezó con el diagnóstico tanto interno como externo de la empresa REGENERAMED S.A. por lo que se procedió a usar herramientas de investigación donde el cual se determinó que la empresa no tiene un organigrama para poder tener la jerarquía organizacional, construir un FODA para poder diseñar un plan estratégico para que sea un incremento en las gestiones administrativas y operarias.
Se realizaron encuestas y entrevistas, lo que se concluyó que no mantiene un organigrama la empresa, manejo de recurso, gestión de talento humano junto con el marketing no estaba enfocado en la fidelización del cliente, por lo que se propuso estrategias para cubrir las falencias.
Esta investigación mantuvo su enfoque en la implementación del plan, la ardua experiencia y capacidad de crecimiento de la empresa además que se generará un impacto en la empresa el cual incrementaría aún más sus probabilidades de nivel de fortalecimiento y mejorará su rentabilidad, es importante que el trabajo en equipo sea entendido por todos los colaboradores de la empresa, de este modo todos orientarán las acciones para alcanzar las metas y objetivos organizacionales.













RECOMENDACIONES

Se recomienda que la empresa REGENERAMED S.A., analizar la implementación de la presente propuesta del plan estratégico para obtener un mejoramiento continuo en las actividades administrativas, operativas y de mercadeo.
        La empresa debe ser muy constante en las evaluaciones y capacitaciones de sus colaboradores en gestión organizacional, administrativa, control de seguridad sanitaria y atención del cliente, se sugiere que todos los empleados que laboran dentro de la organización tengan conocimiento de la misión, visión, metas y políticas estipuladas para alcanzar el objetivo general.
La compañía debe realizar estrategias constantes de publicidad como promociones del servicio para la fidelización de los clientes, independientemente de la competencia. En particular se insinúa en invertir en el diseño de un plan de marketing que sirva para promover y difundir los servicios brindados por la compañía y su alcance permita capturar clientes nuevos y la fidelización de los antiguos, por ende, incrementar la productividad de la empresa.
 Finalmente, se recomienda evaluar a la empresa un año después, esto con la finalidad de averiguar si los cambios que se obtuvieron durante la investigación son consistentes junto con la retroalimentación continua con ajustes si es necesario como va desarrollándose el plan estratégico propuesto. 
