1.2.2.4 Enfoque (clasificación) de los planeadores según Ackoff. 
Para Ackoff se basa en los modelos de planeación según (Miriam, 2003, pp26) en su investigación tenemos lo siguiente:
	Reactivitas. - No gustan de la situación presente ni de la tendencia en futuro; añoran el pasado. Piensan que en el pasado todo fue mejor.
	Inactivitas. - Están satisfechos con las cosas tal como se encuentran. No añoran el pasado, pero tampoco tienen interés en intervenir en el planteamiento de su futuro. Su objetivo es prevenir el cambio, sobrevivir y buscar la permanencia de la situación presente. 
	Preactivos. - Intentan predecir el futuro y se preparan para afrontarlo. Estos planeadores ven al futuro como algo negativo, por lo que hay que prepararse para enfrentarlo.
	Interactivitas o proactivitas. - No desean regresar al pasado, ni permanecer como están o aceptar un futuro caracterizado por las extrapolaciones de las estructuras actuales. Anhelan el diseño de un futuro deseable e inventan o crean medios para acercarse a este. La Planeación normativa, la investigación-acción y algunos enfoques participativos, como la planeación inductiva, caen dentro de esta clasificación.
