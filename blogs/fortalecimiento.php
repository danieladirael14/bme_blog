1.2.1 Concepto de Planeación.
La planeación nos brinda diferentes formas de obtener nuestros objetivos deseados en cualquier experimento tanto como científico o social, por lo que se dan a conocer varios análisis para poder realizar el cálculo de las dificultades que pueda tener el proceso y poder dictaminar algún desenlace de la investigación.
“La planeación es la previsión de escenarios futuros y la determinación de los resultados que se pretende obtener, mediante el análisis del entorno para minimizar riesgos, con la finalidad de optimizar los recursos y definir las estrategias que se requieren para lograr el propósito de la organización con una mayor probabilidad de éxito” (Ramos pp. 5).
	Modelo de Planeación. 
	Modelo de William Newman.
          “La propuesta de William H. Newman proviene de la teoría Clásica de la Administración. Nos puede parecer un modelo simplista, pero tiene la virtud de centrar la atención al hecho de que el proceso de planeación, estratégica o de otra naturaleza, inicia con la precisión de un diagnóstico relativo a un problema determinado. Implica que la planeación debe resolver problemas u objetivos reales, no a simples especulaciones” (Flores & Olivera, 2002, pp2).
Enfatiza en la necesidad de encontrar probables soluciones alternativas, para orientar las actividades de planeación. Posteriormente a la elección de las soluciones alternativas, propone la realización de un pronóstico de resultados para cada acción seleccionada, con el interés de determinar las posibilidades reales de solución de problemas inherentes a ellas; una vez que estas se hayan evaluado, se procede a preparar el objetivo estratégico, dando sentido a la acción planificadora.
